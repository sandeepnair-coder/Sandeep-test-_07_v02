// ── BATTLE CARD LOADER (mirrors Creative Intel loader) ──
function launchBattleCardGeneration(brandName, category, segment) {
  var overlay = document.getElementById('battleLoadingOverlay');
  if (!overlay) return;

  // Set brand name in loader
  var brandEl = document.getElementById('battleAnalysingBrand');
  if (brandEl) brandEl.textContent = brandName;

  // Show overlay
  overlay.classList.add('show');

  // Animate progress bar — slow initial, API will complete it
  var prog = document.getElementById('battleLoadingProgress');
  if (prog) {
    prog.style.animation = 'none';
    prog.offsetHeight;
    prog.style.width = '0%';
    prog.style.transition = 'none';
    prog.offsetHeight;
    prog.style.transition = 'width 0.8s ease';
  }

  // Reset all steps
  [1,2,3,4,5].forEach(function(i) {
    var el = document.getElementById('bstep' + i);
    if (el) el.className = 'lstep';
  });
  var s1 = document.getElementById('bstep1');
  if (s1) s1.className = 'lstep active';
  if (prog) prog.style.width = '8%';

  // Track which step we're on — advance slowly with timers
  var currentStep = 1;
  var stepTimers = [];

  // Step 2 at 3s, Step 3 at 8s, Step 4 at 15s — deliberately slow
  // Step 5 only triggers when API actually responds
  var timings = [[2, 3000, '20%'], [3, 8000, '40%'], [4, 15000, '60%']];
  timings.forEach(function(t) {
    var timer = setTimeout(function() {
      var prev = document.getElementById('bstep' + (t[0] - 1));
      var cur = document.getElementById('bstep' + t[0]);
      if (prev) prev.className = 'lstep done';
      if (cur) cur.className = 'lstep active';
      if (prog) prog.style.width = t[2];
      currentStep = t[0];
    }, t[1]);
    stepTimers.push(timer);
  });

  // Start actual API generation
  generateBattleCardData(brandName, category, segment).then(function() {
    // Clear pending timers
    stepTimers.forEach(clearTimeout);

    // Sequentially complete remaining steps with animation
    var remaining = [];
    for (var i = currentStep; i <= 5; i++) remaining.push(i);

    function completeStep(idx) {
      if (idx >= remaining.length) {
        // All steps done — hold for a moment then navigate
        if (prog) prog.style.width = '100%';
        setTimeout(function() {
          overlay.classList.remove('show');
          _doShowPage('battle');
          showToast('success', 'Battle Cards ready!', brandName + ' competitive playbook generated by AI.');
        }, 800);
        return;
      }
      var stepNum = remaining[idx];
      // Mark previous done, current active
      if (stepNum > 1) {
        var prev = document.getElementById('bstep' + (stepNum - 1));
        if (prev) prev.className = 'lstep done';
      }
      var cur = document.getElementById('bstep' + stepNum);
      if (cur) cur.className = 'lstep active';
      var pct = 60 + (idx + 1) * (40 / (remaining.length + 1));
      if (prog) prog.style.width = Math.min(pct, 95) + '%';

      setTimeout(function() {
        // Mark this one done too
        if (cur) cur.className = 'lstep done';
        completeStep(idx + 1);
      }, 400);
    }

    completeStep(0);
  });
}

